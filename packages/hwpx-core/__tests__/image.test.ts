describe("이미지 삽입", () => {
  it("PNG 이미지를 삽입할 수 있다", async () => {
    const doc = await HwpxDocument.open(skeletonBytes);
    doc.addParagraph("이미지 위의 텍스트");

    const para = doc.addImage(RED_PIXEL_PNG, {
      mediaType: "image/png",
      widthMm: 50,
      heightMm: 50,
    });
    expect(para).toBeDefined();

    doc.addParagraph("이미지 아래의 텍스트");

    const saved = await doc.save();
    expect(saved.byteLength).toBeGreaterThan(0);

    // 재열기하여 이미지 바이너리 확인
    const pkg2 = await HwpxPackage.open(saved);
    const binParts = pkg2.partNames().filter(p => p.startsWith("BinData/"));
    expect(binParts.length).toBeGreaterThanOrEqual(1);

    // PNG 데이터 확인
    const pngPart = binParts.find(p => p.endsWith(".png"));
    expect(pngPart).toBeDefined();
    const pngData = pkg2.getPart(pngPart!);
    expect(pngData.byteLength).toBe(RED_PIXEL_PNG.byteLength);
  });

  it("JPEG 이미지를 삽입할 수 있다", async () => {
    const doc = await HwpxDocument.open(skeletonBytes);
    doc.addImage(WHITE_PIXEL_JPEG, {
      mediaType: "image/jpeg",
      widthMm: 100,
      heightMm: 75,
    });

    const saved = await doc.save();
    const pkg2 = await HwpxPackage.open(saved);
    const jpgPart = pkg2.partNames().find(p => p.endsWith(".jpg"));
    expect(jpgPart).toBeDefined();
  });

  it("여러 이미지를 삽입할 수 있다", async () => {
    const doc = await HwpxDocument.open(skeletonBytes);

    doc.addImage(RED_PIXEL_PNG, { mediaType: "image/png", widthMm: 30, heightMm: 30 });
    doc.addImage(WHITE_PIXEL_JPEG, { mediaType: "image/jpeg", widthMm: 60, heightMm: 40 });
    doc.addImage(RED_PIXEL_PNG, { mediaType: "image/png", widthMm: 80, heightMm: 80 });

    const saved = await doc.save();
    const pkg2 = await HwpxPackage.open(saved);
    const binParts = pkg2.partNames().filter(p => p.startsWith("BinData/"));
    expect(binParts.length).toBeGreaterThanOrEqual(3);
  });

  it("이미지 삽입 후 ObjectFinder로 찾을 수 있다", async () => {
    const doc = await HwpxDocument.open(skeletonBytes);
    doc.addImage(RED_PIXEL_PNG, { mediaType: "image/png", widthMm: 50, heightMm: 50 });

    const saved = await doc.save();
    const pkg2 = await HwpxPackage.open(saved);
    const finder = new ObjectFinder(pkg2);
    const pics = finder.findPictures();
    expect(pics.length).toBeGreaterThanOrEqual(1);
  });

  it("이미지가 포함된 HWPX를 한/글에서 열 수 있게 저장한다", async () => {
    const doc = await HwpxDocument.open(skeletonBytes);

    doc.addParagraph("이미지 삽입 테스트 문서");
    doc.addParagraph("");

    // Use test pixel image
    const imgData = RED_PIXEL_PNG;
    const mediaType = "image/png";

    doc.addImage(imgData, { mediaType, widthMm: 80, heightMm: 20 });
    doc.addParagraph("");
    doc.addParagraph("이미지가 위에 표시되어야 합니다.");

    const saved = await doc.save();
    const outPath = resolve(__dirname, "..", "image-test.hwpx");
    writeFileSync(outPath, saved);
    console.log(`이미지 테스트 파일 저장: ${saved.byteLength} bytes → ${outPath}`);

    // 재열기 검증
    const doc2 = await HwpxDocument.open(saved);
    expect(doc2.paragraphs.length).toBeGreaterThanOrEqual(3);
  });
});
